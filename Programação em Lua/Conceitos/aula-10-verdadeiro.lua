--[[
TUDO QUE O IF ESPERA É APENAS VERDADEIRO OU FALSO

SE TRUE > EXECUTA O CÓDIGO
SE FALSE > CAI PARA O PRÓXIMO BLOCO

]]--

--true é igual a false > NÃO
--true é diferente de false > SIM > true

if (true ~= false) then
	print("Verdadeiro")
else
	print("Falso")
end

if (10 ~= 10) then
	print("Verdadeiro")
else
	print("Falso")
end

if (1 ~= 5) then
	print("Verdadeiro")
else
	print("Falso")
end
